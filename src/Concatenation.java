public class Concatenation {
    public static void main(String[] args) {
        String fName = "Cory";
        String lName = "Baxter";
        int age = 28;
        System.out.println(fName + " " + lName + " is " + age + " years old!");
        System.out.println(fName + " " + lName + "'s band had a song called \"Feelin' the love\"!");
    }
}
